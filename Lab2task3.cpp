#include <iostream>
#include <math.h>
using namespace std;

int main() {
	double length, width, area;

	// initializing the length and the width
	length = 4;
	width = 3.5;

	// calculating the area
	area = length * width;

	// print the area
	cout << "The area of the rectangle is: " << area << "\n";

	return 0;
}